int DicomDictionary::FindKeyIndex(const DWORD id)
{
    int nCnt = 0;

    if( id == 0x00000000 )
    {
        return -1;
    }

    while ( dicom_dict[nCnt].id != 0x00000000 )
    {
        if ( dicom_dict[nCnt].id == id )
            return nCnt;

        nCnt++;
    }

    return -1;
}

int FindKeyIndexFrom(const DWORD id, int idx)
{
    if( idx >= (int)sizeof ( dicom_dict ) )
        return -2;

    int nCnt = idx;

    if( id == 0x00000000 )
    {
        return -1;
    }

    while( dicom_dict[nCnt].id != 0 )
    {
        if( dicom_dict[nCnt].id == id )
            return nCnt;

        nCnt++;
    }

    return -1;

    return -1;
}

WORD  DicomDictionary::GetVR(const DWORD id, bool* sameavailed )
{
    WORD    nRet   = 0;

    int     nIndex = FindKeyIndex(id);

    if( nIndex >= 0 )
    {
        memcpy( &nRet, &dicom_dict[nIndex].vr, 2 );

        if ( sameavailed != NULL)
        {
            if ( FindKeyIndexFrom( id, nIndex ) > 0 )
            {
                *sameavailed = true;
            }
            else
            {
                *sameavailed = false;
            }
        }
    }

    return nRet;
}

void DicomDictionary::GetVR(const DWORD id, void *pVR, bool* sameavailed)
{
    bool bRet = false;
    WORD nRet = GetVR( id, &bRet );

    if(pVR)
    {
        memcpy( pVR, &nRet, 2 );

        if ( sameavailed != NULL)
        {
            *sameavailed = bRet;
        }
    }
}

int DicomDictionary::GetVRarray( const DWORD id, WORD**pVRarray )
{
    if ( *pVRarray != NULL )
        return -1;

    int nIndex = FindKeyIndex(id);
    int nCount = 0;

    while ( nIndex > 0 )
    {
        *pVRarray = new WORD;
        if ( *pVRarray != NULL )
        {
            memcpy( *pVRarray, &dicom_dict[nIndex].vr, 2 );
            nCount++;

            nIndex = FindKeyIndexFrom( id, nIndex );

            if ( nIndex > 0 )
            {
                //*pVRarray++;
                pVRarray++;
            }
        }
    }

    return nCount;
}

const char* DicomDictionary::GetMean(const DWORD id)
{
    int     nIndex = FindKeyIndex(id);

    if(nIndex >= 0)
    {
        return (const char*)&dicom_dict[nIndex].mean;
    }

    return NULL;
}
